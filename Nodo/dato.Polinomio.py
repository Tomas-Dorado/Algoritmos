class datoPolinomio(object):
    """
    Clase que representa un polinomio.
    """

    def __init__(self, valor, termino):
        """
        Inicializa el polinomio con un valor y un término.
        :param valor: Valor del polinomio.
        :param termino: Término del polinomio.
        """
        self.valor = valor
        self.termino = termino
   
